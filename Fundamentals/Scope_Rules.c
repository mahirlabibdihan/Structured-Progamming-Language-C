// There are 4 C scopes

// Program Scope : Available to all the files of entire program .  // Ex: Global Variables

// File Scope : Starts at the beginning of the file and ends with the end of the file. // Ex: static global variable

// Block scope : Begins with the opening { of a block and ends with its associated closing }  // Ex: Functiion parameter

// Function prototype scope : Identifiers declared in a function prototype; visible within the prototype.

// Function scope : Begins with the opening { of a function and ends with its closing }. // Ex: Label must be within the same function as the goto.